// Define a Logo class.
class Logo {
  // Constructor method to initialize a Logo object with text, text color, and shape color.
  constructor(text, textColor, shapeColor) {
    this.text = text; // Store the text content of the logo.
    this.textColor = textColor; // Store the color of the text.
    this.shapeColor = shapeColor; // Store the color of the shape.
  }
}

// Export the Logo class
module.exports = Logo;
